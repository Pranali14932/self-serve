$(document).ready(function() {
    $("#getStarted").click(function() {
        $("#getStarted").hide();
        $(".div1").fadeIn();
        $(".div2").hide();
        $(".div3").hide();
        $(".div4").hide();
        $(".div5").hide();
    });

    $(".td1").click(function() {
        $("#getStarted").hide();
        $(".div1").hide();
        $(".div2").fadeIn();
        $(".div3").hide();
        $(".div4").hide();
        $(".div5").hide();
    });

    $(".td2").click(function() {
        $("#getStarted").hide();
        $(".div1").hide();
        $(".div2").hide();
        $(".div3").fadeIn();
        $(".div4").hide();
        $(".div5").hide();
    });

    $(".td3").click(function() {
        $("#getStarted").hide();
        $(".div1").hide();
        $(".div2").hide();
        $(".div3").hide();
        $(".div4").fadeIn();
        $(".div5").hide();
    });

    $(".td4").click(function() {
        $("#getStarted").hide();
        $(".div1").hide();
        $(".div2").hide();
        $(".div3").hide();
        $(".div4").hide();
        $(".div5").fadeIn();
    });

    $(".td5").click(function() {
        window.location.href = "contact_form.html";
    })
});

